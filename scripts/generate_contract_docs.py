#!/usr/bin/env python3
from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[1]
TAXONOMY = ROOT / "schemas" / "taxonomy.json"
DEFAULT_OUTPUT = ROOT / "docs" / "generated" / "contract-taxonomy.md"


def load_taxonomy() -> dict[str, Any]:
    return json.loads(TAXONOMY.read_text(encoding="utf-8"))


def table(headers: list[str], rows: list[list[str]]) -> list[str]:
    lines = [
        "| " + " | ".join(headers) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for row in rows:
        lines.append("| " + " | ".join(row) + " |")
    return lines


def bullet_table(values: list[str]) -> list[list[str]]:
    return [[f"`{value}`"] for value in values]


def generate() -> str:
    taxonomy = load_taxonomy()
    warning_rows = [
        [
            f"`{code}`",
            ", ".join(f"`{key}`" for key in taxonomy["warning_metadata_required_keys"][code]),
        ]
        for code in taxonomy["warning_codes"]
    ]
    lines: list[str] = [
        "---",
        "title: Generated Contract Taxonomy",
        "date: 2026-05-26",
        "type: reference",
        "status: generated",
        "---",
        "",
        "# Generated Contract Taxonomy",
        "",
        "Generated by `python3 scripts/generate_contract_docs.py --write` from `schemas/taxonomy.json`.",
        "Do not edit this file by hand.",
        "",
        f"- Schema version: `{taxonomy['schema_version']}`",
        f"- Status: `{taxonomy['status']}`",
        f"- Locked at: `{taxonomy['locked_at']}`",
        "",
        "## Usage And Price Components",
        "",
        *table(["Component"], bullet_table(taxonomy["component_names"])),
        "",
        "## Units",
        "",
        *table(["Unit"], bullet_table(taxonomy["units"])),
        "",
        "## Warning Codes",
        "",
        *table(["Warning code", "Required metadata keys"], warning_rows),
        "",
        "## Alias Resolution Values",
        "",
        *table(["Alias resolution"], bullet_table(taxonomy["alias_resolution_values"])),
        "",
        "## Fixture Scenarios",
        "",
        *table(["Scenario"], bullet_table(taxonomy["fixture_scenarios"])),
        "",
        "## Expected Languages",
        "",
        *table(["Language"], bullet_table(taxonomy["expected_languages"])),
        "",
        "## Debug Decision Types",
        "",
        *table(["Decision type"], bullet_table(taxonomy["debug_decision_types"])),
        "",
    ]
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate taxonomy-derived contract documentation.")
    parser.add_argument("--output", help="Write generated markdown to this path instead of stdout.")
    parser.add_argument("--write", action="store_true", help="Write to docs/generated/contract-taxonomy.md.")
    args = parser.parse_args()

    content = generate()
    if args.write:
        output = DEFAULT_OUTPUT
    elif args.output:
        output = Path(args.output)
    else:
        print(content, end="")
        return 0

    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(content, encoding="utf-8")
    print(f"Wrote generated contract docs to {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
